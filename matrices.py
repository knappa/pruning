def make_rate_constraint_matrix(pis):
    pi_c, pi_g, pi_t, pi_a = pis
    return np.array(
        [
            2 * pi_a * pi_c,
            2 * pi_a * pi_g,
            2 * pi_a * pi_t,
            2 * pi_c * pi_g,
            2 * pi_c * pi_t,
            2 * pi_g * pi_t,
        ]
    )


def make_rate_constraint_matrix_gtr16(pis):
    pi_c, pi_g, pi_t, pi_a = pis
    # works for phased and unphased
    return np.array(
        [
            4 * pi_a * pi_c,
            4 * pi_a * pi_g,
            4 * pi_a * pi_t,
            4 * pi_c * pi_g,
            4 * pi_c * pi_t,
            4 * pi_g * pi_t,
        ]
    )


